package cycletask;

public class While {

//    используя while вывести в консоль все числа от 0 до 20 в 1 строку через пробел

    public int methodWhile() {
            int num = 0;
            while (num <= 20) {
                System.out.println(num + " ");
                num++;
            }
        return num;
    }

}
