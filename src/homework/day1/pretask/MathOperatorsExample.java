package homework.day1.pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int x = 31;
        int y = 15;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println((double) x / (double) y);
    }
}
