package pretask;

public class MultiplicationTable {
    public static void main(String[] args) {
        //используя цикл for, выведите на экран таблицу умножения для числа 5 (1 * 5, 2 * 5, и так далее)
        for (int i = 1; i <= 5; i++) {
            System.out.println(i + " * 5");
        }
    }
}
