package pretask;

public class SimpleLoopExample {
    public static void main(String[] args) {
        //объявите целочисленную переменную и используйте цикл for, чтобы вывести числа от 1 до этой переменной
        int h = 20;
        for (int i = 0; i < h; i++) {
            System.out.println(i);
        }
    }
}
